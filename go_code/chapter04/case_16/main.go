package main

import "fmt"

func main()  {
	/*
		Golang 中有 3 个位运算符
	1. 按位与&: 两位全是 1，结果全是 1，否则为0
	2. 按位或|: 两位一个为 0，一个为 1，结果为 1，否则为 0
	*/
	// 位运算的演示(对比补码)
	fmt.Println(2&3) // 2
	fmt.Println(2|3) // 3
	fmt.Println(2^3) // 1
	fmt.Println(-2^2) // -2 补码 [1111 1110]
	/*
		Golang 中有两个移位运算符：
	1. 右移运算符 >>: 低位溢出，符号位不变，并且用符号位补溢出的高位
	2. 左移运算符 <<: 符号位不变，低位补 0
	*/
	a := 1 >> 2 // 0000 0001 => 0000 0000 = 0
	b := 1 << 2 // 0000 0001 => 0000 0100 = 4
	fmt.Println(a, b)
}
